                            <h1 class="title text-bold after-left-l mb-45">Queratocono</h1>
                            <p>El Queratocono o Keratocono es una enfermedad ocular progresiva que causa un adelgazamiento de la córnea, (el tejido transparente ubicado por delante del iris y la pupila.) Al progresar el Queratocono la curvatura de la córnea se hace irregular y consecuentemente la calidad de la visión se deteriora, por lo que las personas no alcanzan una visión satisfactoria con lentes ni con lentes de contacto.
									
									
									<br><br><i><h6 class="color-blue text-bold">Causas</h6></i>
								   Aunque la causa de la enfermedad se desconoce, hay evidencia que sugiere la existencia de factores genéticos, alérgicos y ambientales que pueden contribuir al desarrollo de la enfermedad (por ej: frotarse los ojos puede desformar la corneas). Si existen antecedents familiares de queratocono el paciente se encuentra en mayor riesgo y, por lo tanto, se debe tener especial cuidado en realizar controles periódicos para poder detectarlo a tiempo.
								   
									<br><br><strong>Aproximadamente el 90% de los casos afectan ambos ojos.</strong>
									Los investigadores estiman que el Queratocono afecta aproximadamente 1 de cada 2.000 personas. La enfermedad afecta hombres y mujeres por igual y no se conocen diferencias raciales
									
									<br><br><i><h6 class="color-blue text-bold">Edad en que aparece</h6></i>
									Puede empezar a manifestarse entre la edad de 8 a 45 años. En la mayoría de los casos se hace evidente durante la adolescencia y empeora lentamente para estabilizarse entre los 30 y 40 años, aunque en algunas personas tiene un curso más acelerado y agresivo.
								   
								   <br><i><h6 class="color-blue text-bold">Síntomas</h6></i>
									En etapas tempranas el paciente puede notar un leve deterioro en la vision. Al progresar la enfermedad la visión fluctúa y pueden presentar cambios frecuentes en la medida de sus anteojos o lentes de contacto (debido al adelgazamiento de la córneo y a la fuerza ejercida por la presión ocular)
									En etapas más avanzadas los rayos de luz que entran al ojo sufren una distorsión significativa, llamada astigmatismo irregular que ya no puede ser compensada con anteojos.

									 <br><br><i><h6 class="color-blue text-bold">Tratamiento</h6></i>
									Hasta hace poco tiempo la única alternativa de solución era un transplante de córnea, procedimiento quirúrgico invasivo y que requiere tejido donante que se obtiene con mucha dificultad en nuestro medio. Actualmente existen modernas opciones de tratamiento que permiten una intervención más temprana y mejores resultados.
									<br><br>
									<ul class="circulo">
									<li>Cross Linking Corneal (C3-R®): Para las personas que adolecen de Queratocono leve o moderado.</li>
									<li>Implantes corneales INTACS: En caso de pacientes con Queratocono moderado, que evidencien irregularidad de la superficie corneal, los insertos INTACS son la mejor opción.</li>
									</ul>
									</p>
