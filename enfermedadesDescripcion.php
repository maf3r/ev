                            <h1 class="title text-bold after-left-l mb-45">Correción de refracción mediante técnica LASIK</h1>
                            <p>Hace más de 30 años, el profesor J.I. Barraquer comenzó a practicar técnicas de corrección quirúrgica de disfunciones visuales que se han impuesto mundialmente. Hoy es muy frecuente la cirugía LASIK -keratomileusis in situ automatizada con Excimer Láser-, perfeccionada en Colombia por el profesor Luis A. Ruiz, que permite cambiar la forma de la córnea.
									<br><br>							
									LASIK es la técnica de corrección de Miopía, Hipermetropía y Astigmatismo con Láser más difundida y aceptada. Actualmente se tiene más de quince años de experiencia y perfeccionamiento.
									
									<br><br><i><h6 class="color-blue text-bold">Requisitos</h6></i>
								   Ponemos gran atención y cuidado en la selección adecuada de candidatos para el procedimiento LASIK. Para poder confirmar, con seguridad, si es posible operarle es indispensable una consulta y exámenes complementarios.  Como regla general, el paciente debe ser mayor de 18 años y el defecto visual debe haberse mantenido estable por al menos 1 año.
								   
									<br><br><i><h6 class="color-blue text-bold">Evaluación Previa</h6></i>
									Es necesario realizar una consulta oftalmológica que permita a los doctores hacer una evaluación completa del estado de sus ojos y confirmar si el procedimiento LASIK es el tratamiento adecuado para ud. Se requiere, además, efectuar varios estudios complementarios de Topografía Computarizada de cornea, medición de Espesor Corneal y Aberrometría Óptica computarizada  para poder alcanzar los resultados buscados.  Si utiliza lentes de contacto debe suspender su uso mínimo 4 días previa a la evaluación.
									
									<br><br><i><h6 class="color-blue text-bold">Tratamiento LASIK</h6></i>
									<ul class="circulo">
									<li>En Humana Visión las realizamos los días viernes.</li>
									<li>Se realiza la corrección simultánea de ambos ojos en el mismo día.</li>
									<li>El tiempo total del procedimiento es de, aproximadamente, 7 minutos por cada ojo.</li>
									<li>La permanencia en el Centro de Láser es de aproximadamente una hora.</li>
									<li>Se realiza una consulta post-operatoria esa misma tarde.</li>
									</ul>
								   
								   <br><i><h6 class="color-blue text-bold">Cuidados Post-Operatorios</h6></i>
									<ul class="circulo">
									<li>Puede retomar su vida normal al día siguiente del procedimiento.</li>
									<li>Debe evitar zambullirse en piscinas o mar por el primer mes.</li>
									<li>Debe protegerse con gafas  en ambientes al aire libre.</li>
									<li>El procedimiento LASIK es un procedimiento quirúrgico y los tiempos de recuperación y cicatrización entre personas varían. La visión irá mejorando con el paso de los días.</li>
