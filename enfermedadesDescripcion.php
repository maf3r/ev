                             <h1 class="title text-bold after-left-l mb-45">Pterigion</h1>
                            <p>Los Pterigion son lesiones que crecen en el ángulo interno de lo ojos e invaden la córnea (tejido transparente delante del iris y la pupila). Pueden ser ocasionados por el exceso de exposición al sol o por factores genéticos.
									
									
									<br><br><i><h6 class="color-blue text-bold">Tratamiento</h6></i>
								   No pueden eliminarse con gotas ni medicamentos y la única solución definitiva es extirparlos con cirugía convencional.
								   
									<br><br><i><h6 class="color-blue text-bold">Operación</h6></i>
									Los mejores resultados se obtienen con una técnica que requiere el colocar un injerto de conjuntiva del mismo paciente. De esta manera tenemos resultados exitosos en más del 98% de los casos (sin que el pterigion vuelva a salir).
									En caso de Pterigion extremadamente grande, que cubra la pupila, estos criterios cambian pues el paciente puede requerir un transplante de córnea. Una extirpación oportuna del Pterigion es una operación relativamente sencilla que le evitará complicaciones innecesarias. 
									
									<br><br><i><h6 class="color-blue text-bold">Post Operatorios</h6></i>
									Quedan unos puntos de sutura que se deben retirar 10 días después de la operación. Durante el mes siguiente la persona debe evitar exponerse a ambientes contaminados, sol, viento y situaciones que impliquen riesgo de golpes sobre los ojos.
									</p>
