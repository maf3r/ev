                             <h1 class="title text-bold after-left-l mb-45">Catarata</h1>
							 <p>La Catarata es la opacificación total o parcial del cristalino (lente natural del ojo) que reduce progresivamente la visión. La catarata está asociada normalmente a la edad. Una catarata puede ser la causa por la que una imagen nítida se vuelva borroso, los colores brillantes se apaguen, o la visión nocturna disminuya; o la razón por la que los anteojos de lectura a los progresivos, parezcan no servir ya de ayuda 
									<br><br>							
									<strong>Desafortunadamente, no es posible prevenir la catarata</strong> y ningún medicamento, dieta o láser pueden eliminarla.
									
									<br><br><i><h6 class="color-blue text-bold">Opciones de tratamientos</h6></i>
								   El mejor tratamiento es extraer el cristalino opacificado y sustituirlo por un lente intraocular mediante una intervención. Habitualmente se implantan lentes monofocales que, si bien proporciona una buna visión de lejos, no permiten enfocar a corta y media distancia, por lo que se hace necesario el uso de algún tipo de anteojos para leer. Existen DOS técnicas para este procedimiento:
								   
									<br><i><h6 class="color-blue text-bold">Técnica de Facoemulsificación:</h6></i>									
									<ul class="circulo">
									<li>Técnica utilizada con mayor frecuencia.</li>
									<li>Conocida también como técnica de ultrasonido o AquaLase.</li>
									<li>No requiere suturas.</li>
									<li>Recuperación rápida (pocos dias) y sin restricciones de tocarse los ojos.</li>
									<li>Implante de lente intraocular flexible monofocal.</li>									
									<li>Es probable que se necesite utilizar anteojos para visión de cerca.</li>
									<li>Se utiliza, generalmente, en cataratas leves o moderadas.</li>									
									</ul>
								   
								   <br><i><h6 class="color-blue text-bold">Técnica Convencional</h6></i>
									<ul class="circulo">
									<li>Requiere Suturas e Implante de lente convencional Rígido.</li>
									<li>Requiere varias semanas de reposo y recuperación.</li>
									<li>Se implantan lentes monofocales que proporcionan buena visión de lejos</li>
									<li>Es probable que sea necesario utilizar anteojos para visión media y de cerca.</li>
									<li>Se asocia con astigmatismos significativos.</li>																
									<li>Debe tener cuidado de no tocarse el ojo o realizar esfuerzos.</li>
									<li>Es la técnica más económica.</li>
									</ul>
									
									<br><br><i><h6 class="color-blue text-bold">Recomendación:</h6></i>
									Habitualmente recomendamos operar un ojo a la vez y las personas tienden a esperar 1 o 2 semanas entre cada operación.
									
									
									<br><br><i><h6 class="color-blue text-bold">Contraindicaciones:</h6></i>
									Hemos realizado cirugías de catarata en pacientes mayores a 100 años y en personas con diabetes. En general no existe ninguna contra indicación, pero lo aconsejable es conversarlo con el doctor durante la consulta. 
									</p> <hr>                      
